package com.bwf.entity;

public class ModuleOption {
	
	
	
	private Integer moduleOptionId;
	
	private Integer affairModuleId;
	
	private Integer optionId;
	
	private Integer order;
	
	private String optionName;
	
	private String optionData;

	public String getOptionData() {
		return optionData;
	}

	public void setOptionData(String optionData) {
		this.optionData = optionData;
	}

	public Integer getModuleOptionId() {
		return moduleOptionId;
	}

	public void setModuleOptionId(Integer moduleOptionId) {
		this.moduleOptionId = moduleOptionId;
	}

	public Integer getAffairModuleId() {
		return affairModuleId;
	}

	public void setAffairModuleId(Integer affairModuleId) {
		this.affairModuleId = affairModuleId;
	}

	public Integer getOptionId() {
		return optionId;
	}

	public void setOptionId(Integer optionId) {
		this.optionId = optionId;
	}

	public Integer getOrder() {
		return order;
	}

	public void setOrder(Integer order) {
		this.order = order;
	}

	public String getOptionName() {
		return optionName;
	}

	public void setOptionName(String optionName) {
		this.optionName = optionName;
	}
	
	
	

}
